public class Location {

    public int square;
    public int subSquare;

    public Location() {
        int square = 0;
        int subSquare = 0;
    }

    public Location(int aSquare, int aSubSquare) {
        this.square = aSquare;
        this.subSquare = aSubSquare;
    }

}
